<template>
  <label
    class="inline-flex items-center space-x-3 cursor-pointer"
    :class="{ 'opacity-50 cursor-default': disabled }"
  >
    <span class="relative">
      <span
        class="block w-10 h-6 rounded-full shadow-inner"
        :class="modelValue ? 'bg-indigo-500' : 'bg-gray-400 dark:bg-gray-600'"
      />
      <span
        class="
          block
          absolute
          inset-y-0
          left-0
          mt-1
          ml-1
          w-4
          h-4
          bg-white
          rounded-full
          shadow-lg
          transition-transform
          duration-300
          ease-in-out
        "
        :class="{ 'translate-x-full': modelValue }"
      >
        <input
          v-model="value"
          :disabled="disabled"
          type="checkbox"
          class="absolute w-0 h-0 opacity-0"
        />
      </span>
    </span>
    <span class="text-sm"><slot /></span>
  </label>
</template>

<script lang="ts">
import useModelWrapper from '@/composables/use-model-wrapper'
import { defineComponent } from 'vue'

export default defineComponent({
  name: 'AppSwitch',
  props: {
    modelValue: {
      type: Boolean,
      required: false,
      default: false
    },

    disabled: {
      type: Boolean,
      required: false,
      default: false
    }
  },
  emits: ['update:modelValue'],
  setup(props, { emit }) {
    const value = useModelWrapper(props, emit, 'modelValue')

    return {
      value
    }
  }
})
</script>
